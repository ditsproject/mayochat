// Cache hasil lookup biar ga perlu hit Worker terus
const invUsernameCache = new Map();

async function checkRobloxUsername(username) {
    if (!username.trim()) return { ok: false, msg: 'Username kosong' };
    const clean = username.trim().replace(/\s+/g, '').toLowerCase();

    // Cek cache dulu
    if (invUsernameCache.has(clean)) {
        return invUsernameCache.get(clean);
    }

    const fetchWithTimeout = async (ms) => {
        const controller = new AbortController();
        const timer = setTimeout(() => controller.abort(), ms);
        try {
            const res = await fetch(ROBLOX_PROXY_URL, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ usernames: [clean] }),
                signal: controller.signal,
            });
            clearTimeout(timer);
            if (!res.ok) throw new Error('Worker error ' + res.status);
            return await res.json();
        } catch(e) {
            clearTimeout(timer);
            throw e;
        }
    };

    // Coba 1x dengan timeout 5 detik, kalau gagal retry 1x lagi
    for (let i = 0; i < 2; i++) {
        try {
            const data = await fetchWithTimeout(5000);
            if (data?.data && data.data.length > 0) {
                const user = data.data[0];
                const result = { ok: true, id: user.id, name: user.name };
                invUsernameCache.set(clean, result);
                return result;
            }
            const notFound = { ok: false, msg: `"${clean}" tidak ditemukan di Roblox` };
            invUsernameCache.set(clean, notFound);
            return notFound;
        } catch(e) {
            if (i === 1) return { ok: 'warn', msg: 'Koneksi lambat, coba lagi' };
            await new Promise(r => setTimeout(r, 500));
        }
    }
}

// Live username lookup
let invUserLookupTimer = null;
let invLookupRunning = false;

function invTriggerUserLookup(username) {
    const empty   = document.getElementById('invUserEmpty');
    const found   = document.getElementById('invUserFound');
    const err     = document.getElementById('invUserErr');
    const loading = document.getElementById('invUserLoading');
    const lookup  = document.getElementById('invUserLookup');
    if (!lookup) return;

    const show = (el) => {
        [empty, found, err, loading].forEach(e => { if(e) e.style.display = 'none'; });
        if (el) el.style.display = 'flex';
    };

    if (!username || username.trim().length < 3) {
        show(empty);
        lookup.style.borderColor = '';
        return;
    }

    clearTimeout(invUserLookupTimer);
    show(loading);

    invUserLookupTimer = setTimeout(async () => {
        if (invLookupRunning) return;
        invLookupRunning = true;
        const result = await checkRobloxUsername(username);
        invLookupRunning = false;
        if (result.ok === true) {
            document.getElementById('invUserName').textContent = result.name;
            document.getElementById('invUserAvatar').src =
                `${ROBLOX_PROXY_URL}/avatar?userId=${result.id}`;
            const premIcon = document.getElementById('invUserPremium');
            if (premIcon) premIcon.style.display = 'none';
            lookup.style.borderColor = 'var(--success)';
            show(found);
            // Cek premium di background
            fetch(`${ROBLOX_PROXY_URL}/premium?userId=${result.id}`)
                .then(r => r.json())
                .then(data => {
                    if (premIcon && data.isPremium === true) {
                        premIcon.style.display = 'inline-block';
                    }
                })
                .catch(() => {});
        } else {
            document.getElementById('invUserErrTxt').textContent = result.msg;
            lookup.style.borderColor = result.ok === 'warn' ? 'var(--warning)' : 'var(--danger)';
            show(err);
        }
    }, 800);
}

function invResetUserLookup() {
    invLookupRunning = false;
    clearTimeout(invUserLookupTimer);
    const empty   = document.getElementById('invUserEmpty');
    const found   = document.getElementById('invUserFound');
    const err     = document.getElementById('invUserErr');
    const loading = document.getElementById('invUserLoading');
    const lookup  = document.getElementById('invUserLookup');
    if (!lookup) return;
    [found, err, loading].forEach(e => { if(e) e.style.display = 'none'; });
    if (empty) empty.style.display = 'flex';
    lookup.style.borderColor = '';
    const premIcon = document.getElementById('invUserPremium');
    if (premIcon) premIcon.style.display = 'none';
}

function invGenerateInvoice() {
    const robuxRaw = document.getElementById('fRobux').value.trim();
    const user     = document.getElementById('fUser').value.trim();
    const pass     = document.getElementById('fPass').value.trim();
    const codesRaw = document.getElementById('fCodes').value;

    if (!robuxRaw && !user && !pass) { showToast('❌ Isi dulu data customernya!'); return; }

    const robux = invFormatRobux(robuxRaw) || robuxRaw || '???';
    const codes = invExtractCodes(codesRaw);
    const cp = [];
    for (let i = 0; i < 5; i++) cp.push(codes[i] || '???');

    const invoice =
        'DETAIL PESANAN KAMU\n\n' +
        '✨ Jumlah Robux: `' + robux + '`\n' +
        '👤 Username: `' + (user || '???') + '`\n' +
        '🔑 Password: `' + (pass || '???') + '`\n' +
        '🛡 Backup Code: `' + cp.join(', ') + '`';

    invLastInvoiceText = invoice;
    const box = document.getElementById('invOutputBox');
    const esc = invoice.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
    box.innerHTML = esc.replace(/`([^`]+)`/g, '<span class="inv-code-span">`$1`</span>');
    showToast('✅ Invoice dibuat!');
}

function invCopyInvoice() {
    if (!invLastInvoiceText) { showToast('❌ Generate invoice dulu!'); return; }
    const doCopy = (t) => {
        const ta = document.createElement('textarea');
        ta.value = t; ta.style.cssText = 'position:fixed;left:-9999px';
        document.body.appendChild(ta); ta.select();
        document.execCommand('copy'); document.body.removeChild(ta);
        showToast('✅ Invoice dicopy! Paste ke Telegram 🎉');
    };
    if (navigator.clipboard && window.isSecureContext) {
        navigator.clipboard.writeText(invLastInvoiceText)
            .then(() => {
                showToast('✅ Invoice dicopy! Paste ke Telegram 🎉');
            })
            .catch(() => doCopy(invLastInvoiceText));
    } else {
        doCopy(invLastInvoiceText);
    }
}

